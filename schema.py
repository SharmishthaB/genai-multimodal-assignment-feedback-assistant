def validate_pair(pair_dict: dict) -> tuple[bool, List[str]]:
    """
    Validate a single pair against the schema.
    
    Returns:
        (is_valid, list_of_errors)
    """
    errors = []
    
    required_fields = ["topic", "student_work", "identified_errors", "teacher_feedback", "error_severity"]
    for field in required_fields:
        if field not in pair_dict or not pair_dict[field]:
            errors.append(f"Missing or empty required field: {field}")
    
    if pair_dict.get("error_severity") not in ["minor", "moderate", "major"]:
        errors.append(f"Invalid error_severity: {pair_dict.get('error_severity')}")
    
    # Check for solution leakage
    leakage_keywords = [
        "the answer is", "therefore", "the solution is", "= [", 
        "the correct formula", "solving this gives"
    ]
    feedback = pair_dict.get("teacher_feedback", "").lower()
    if any(kw in feedback for kw in leakage_keywords):
        errors.append("⚠️  Potential solution leakage detected in teacher_feedback")
    
    is_valid = len(errors) == 0
    return is_valid, errors


def validate_jsonl(jsonl_path: str) -> tuple[int, int, List[tuple[int, List[str]]]]:
    """
    Validate entire JSONL file.
    
    Returns:
        (total_lines, valid_count, list_of_errors_with_line_numbers)
    """
    valid_count = 0
    errors_list = []
    
    with open(jsonl_path, 'r', encoding='utf-8') as f:
        for line_num, line in enumerate(f, 1):
            try:
                data = json.loads(line.strip())
                is_valid, errors = validate_pair(data)
                
                if is_valid:
                    valid_count += 1
                else:
                    errors_list.append((line_num, errors))
            
            except json.JSONDecodeError as e:
                errors_list.append((line_num, [f"JSON parse error: {str(e)}"]))
    
    total_lines = line_num
    return total_lines, valid_count, errors_list


